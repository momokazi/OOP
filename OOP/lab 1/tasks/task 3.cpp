#include <iostream>
using namespace std;

int main(){
	
	int a;
	cout<<"Enter a number between 1-5."<<endl;
	cin>>a;
	
	if(a==1)
	cout<<"Roman numeral is I";
	
	else if(a==2)
	cout<<"Roman numeral is II";
	
	else if(a==3)
	cout<<"Roman numeral is III";
	
	else if(a==4)
	cout<<"Roman numeral is IV";
	
	else if(a==5)
	cout<<"Roman numeral is V";
	
	else
	cout<<"Out of given range! ";

return 0;

}

